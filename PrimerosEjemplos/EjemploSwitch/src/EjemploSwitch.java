public class EjemploSwitch{
    public static void main(String args[]){
        int j = 2;
        switch (j) {
            case 5:
                System.out.println("********");
            break;
                case 4:
                System.out.println("********");
            break;
                case 3:
                System.out.println("********");
            break;
                case 2:
                System.out.println("********");
            break;
                case 1:
                System.out.println("********");
        break;
        }
    }
}